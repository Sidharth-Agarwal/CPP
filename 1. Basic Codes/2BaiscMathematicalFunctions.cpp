#include<iostream>

using namespace std;

int main()
{
    int a,b;

    // Addition
    int c;
    c=a+b;
    cout<<"The addition of the two numbers is : "<<c;
    cout<<endl;

    // Subtration
    int d;
    d=a-b;
    cout<<"The subtraction of the two numbers is : "<<d;
    cout<<endl;

    // Multiplication
    int e;
    e=a*b;
    cout<<"The multiplication of the two numbers is : "<<e;
    cout<<endl;

    // Division
    float f;
    f=a/b;
    cout<<"The division of the two numbers is : "<<f;
    cout<<endl;

    // Modulus
    int g;
    g=a%b;
    cout<<"The modulus of the two numbers is : "<<g;
    cout<<endl;
}